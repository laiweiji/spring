public class Stock {

    private Integer sid;

    private String name;

    private Integer Scount;

    public Integer getSid() {
        return sid;
    }

    public void setSid(Integer sid) {
        this.sid = sid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getScount() {
        return Scount;
    }

    public void setScount(Integer scount) {
        Scount = scount;
    }
}
